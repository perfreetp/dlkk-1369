export const getSpeciesById = (id: string): Species | undefined => {
  return mockSpeciesList.find(s => s.id === id);
};

export const getSeasonalSpecies = (season: '春' | '夏' | '秋' | '冬'): Species[] => {
  const monthMap: Record<string, number[]> = {
    '春': [3, 4, 5],
    '夏': [6, 7, 8],
    '秋': [9, 10, 11],
    '冬': [12, 1, 2],
  };
  const months = monthMap[season];
  return mockSpeciesList.filter(s => s.bestSeason.some(m => months.includes(m)));
};

export const getSpeciesByCategory = (category: string): Species[] => {
  if (category === '全部') return mockSpeciesList;
  return mockSpeciesList.filter(s => s.category === category);
};
